package day24_office_hours;

public class CountNumbers {

    public static void printNumbers(int printNumbers){
        for (int i = 0; i <= printNumbers; i++) {
            System.out.print(i+" ");


        }


    }

    public static void main(String[] args) {
        printNumbers(5);
        System.out.println();
        printNumbers(10);
    }





}
