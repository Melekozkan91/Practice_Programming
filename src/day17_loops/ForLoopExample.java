package day17_loops;

public class ForLoopExample {
    public static void main(String[] args) {


        for (int i =1; i<=10; i++){
            System.out.println(i);


    }
        for (int i=1; i<=10;i++ ){
            System.out.println(i);
        }
        int num =1;
        while (num<=10){
            System.out.println(num);
            num++;
        }





    }
}
