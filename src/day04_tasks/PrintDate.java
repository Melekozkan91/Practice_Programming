package day04_tasks;

public class PrintDate {
    public static void main(String[] args) {
        byte day = 16;
        int month = 02;
        int years = 2023;

        System.out.println(month + "/" + day + "/" + years);
        System.out.println(month + "_" + day + "_" + years);
        System.out.println(month + "|" + day + "|" + years);
        System.out.println(month + "<>" + day + "<>" + years);
        System.out.println("------------------------------");

    }


}