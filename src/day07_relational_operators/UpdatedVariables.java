package day07_relational_operators;

public class UpdatedVariables {
    public static void main(String[] args) {
        int a = 10;  // declared and assigned value
        a =11;      // reassign

        a++;        // reassign a = a + 1;
        System.out.println(a);
        ++a;        // reassign a= a  + 1;
        System.out.println(a);

        int x = 10;
        x= x+ 4;  //14

        x+=4;   //18
        System.out.println(x);















    }







}
